class CurrentPlayerStatus:
    def __init__(self, name: str, score1: str, score2: str):
        self.name = name
        self.score1 = score1
        self.score2 = score2

    def current_player(player):
        name = player

    def get_player(self):
        return self.name

    def current_score1(points):
        score1 = points

    def get_score1(self):
        return self.score1

    def current_score2(points):
        score2 = points

    def get_score2(self):
        return self.score2
